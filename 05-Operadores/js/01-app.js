// Operador Mayor que y Menor que

const numero1 = 20;
const numero2 = '20';
const numero3 = 30;

// Mayor que
console.log(numero1 > numero2); // false

// Mayor o igual que
console.log(numero1 >= numero2); // true

// Menor que
console.log(numero1 < numero2); // false

// Menor o igual que
console.log(numero1 <= numero2); // true