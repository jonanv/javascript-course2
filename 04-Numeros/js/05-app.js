// Incremento y Decremento

let resultado;

// Incremento
resultado = 10;
resultado++; // resultado = resultado + 1
console.log(resultado); // 11

// Decremento
resultado = 10;
resultado--; // resultado = resultado - 1
console.log(resultado); // 9

// Incremento y Decremento en una sola línea
resultado = 10;
resultado += 5; // resultado = resultado + 5
console.log(resultado); // 15

// Preincremento y Postincremento
resultado = 10;
console.log(++resultado); // 11 (preincremento)
console.log(resultado++); // 11 (postincremento)
console.log(resultado); // 12 (resultado después del postincremento)

// Predecremento y Postdecremento
resultado = 10;
console.log(--resultado); // 9 (predecremento)
console.log(resultado--); // 9 (postdecremento)
console.log(resultado); // 8 (resultado después del postdecremento)
