// Orden de operaciones

let resultado;

// 1. Paréntesis
resultado = (2 + 3) * 4; // 20
console.log(resultado);

// 2. Exponentes
resultado = 2 + 2 ** 3; // 10
console.log(resultado);

// 3. Multiplicación y división
resultado = 2 + 3 * 4; // 14
console.log(resultado);

// 4. Suma y resta
resultado = 2 + 3 - 4; // 1
console.log(resultado);

// 5. Concatenación
resultado = 2 + '3' + 4; // 234
console.log(resultado);
