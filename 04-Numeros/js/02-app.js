// Creación de números

const numero1 = 30;
const numero2 = 20;

let resultado;

// Suma
resultado = numero1 + numero2;
console.log('Suma: ', resultado);

// Resta
resultado = numero1 - numero2;
console.log('Resta: ', resultado);

// Multiplicación
resultado = numero1 * numero2;
console.log('Multipliación: ', resultado);

// División
resultado = numero1 / numero2;
console.log('División: ', resultado);

// Módulo
resultado = numero1 % numero2;
console.log('Módulo: ', resultado);

// Potencia
resultado = numero1 ** 2;
console.log('Potencia: ', resultado);

// Incremento
let numero3 = 10;
numero3++;
console.log('Incremento: ', numero3);

// Decremento
let numero4 = 10;
numero4--;
console.log('Decremento: ', numero4);
